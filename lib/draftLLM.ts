type WebsiteIdentity = {
  url: string | null;
  text: string;
};

async function fetchWebsiteIdentity(collectionName: string): Promise<WebsiteIdentity | null> {
  // The cr_agent auto-ingest writes a single brand_guide doc with
  // filename "auto-company-profile" and metadata { is_cr_agent: "true",
  // company_url: <url> }. That doc is the canonical "who you are"
  // anchor — pulled straight from the website the user submitted in
  // Frame 1 and confirmed in Frame 3.
  const matches = await listDocuments({
    collectionName,
    filter: { doc_type: "brand_guide", is_cr_agent: "true" },
    limit: 1,
  });
  if (matches.length === 0) return null;
  const doc = matches[0];
  const url = doc.metadata.company_url ? String(doc.metadata.company_url) : null;
  try {
    const content = await getDocumentContent(collectionName, doc.path);
    return { url, text: (content ?? "").slice(0, 3_000) };
  } catch {
    return { url, text: "" };
  }
}

async function fetchSupplementaryContext(params: {
  collectionName: string;
  signalBody: string;
}): Promise<{ snippets: Array<{ label: string; text: string }>; evidence: string[] }> {
  // Everything EXCEPT the cr_agent identity doc. These supplement the
  // sales pitch with extra past wins, voice samples, etc.
  const candidates = await topDocuments({
    collectionName: params.collectionName,
    query: params.signalBody,
    k: MAX_GROUNDING_DOCS,
    filter: {
      doc_type: { $in: ["brand_guide", "won_deal", "icp", "case_study", "competitor_intel"] },
    },
  });

  const snippets: Array<{ label: string; text: string }> = [];
  const evidence: string[] = [];

  for (const d of candidates) {
    // Skip the cr_agent identity doc — we render it separately.
    if (String(d.metadata.is_cr_agent ?? "") === "true") continue;
    const label = String(d.metadata.filename ?? d.metadata.story_name ?? d.metadata.doc_type ?? "doc");
    evidence.push(label);
    try {
      const content = await getDocumentContent(params.collectionName, d.path);
      if (content) {
        const trimmed = content.slice(0, MAX_GROUNDING_CHARS_PER_DOC).replace(/\s+/g, " ").trim();
        snippets.push({ label, text: trimmed });
      }
    } catch {
      // skip — keep going
    }
  }

  return { snippets, evidence };
}

export async function generateDraft(params: {
  collectionName: string;
  signal: SignalInfo;
}): Promise<GeneratedDraft> {
  const [identity, { snippets, evidence }] = await Promise.all([
    fetchWebsiteIdentity(params.collectionName),
    fetchSupplementaryContext({
      collectionName: params.collectionName,
      signalBody: params.signal.body,
    }),
  ]);

  const apiKey = process.env.GOOGLE_GENAI_API_KEY;
  if (!apiKey) return templateDraft(params.signal, evidence);

  const genai = new GoogleGenerativeAI(apiKey);
  const modelName = process.env.GEMINI_MODEL || "gemini-2.5-flash-lite";
  const model = genai.getGenerativeModel({
    model: modelName,
    generationConfig: {
      responseMimeType: "application/json",
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      responseSchema: DRAFT_SCHEMA as any,
      temperature: 0.6,
    },
  });

  const websiteBlock = identity
    ? [
        identity.url ? `Website URL: ${identity.url}` : "",
        identity.text || "(no website summary available)",
      ]
        .filter(Boolean)
        .join("\n\n")
    : "(no website identity was captured during onboarding — infer a neutral B2B identity from the signal alone)";

  const supplementaryBlock =
    snippets.length === 0
      ? "(no supplementary documents — rely on the YOUR COMPANY section above)"
      : snippets
          .map((s, i) => `--- doc #${i + 1}: ${s.label} ---\n${s.text}`)
          .join("\n\n");

  const userPrompt = [
    SYSTEM_PROMPT,
    "",
    "## YOUR COMPANY (FROM WEBSITE) — IDENTITY ANCHOR",
    "This came from the website URL you entered during onboarding.",
    "The sign-off MUST be derived from this section.",
    "",
    websiteBlock,
    "",
    "## SUPPLEMENTARY CONTEXT (uploaded docs — extra past wins, voice samples)",
    "Use these for facts and tone, NOT for the sign-off identity.",
    "",
    supplementaryBlock,
    "",
    "## SIGNAL POST",
    `Source: ${params.signal.source}`,
    `Author handle: ${params.signal.handle}`,
    params.signal.context ? `Context: ${params.signal.context}` : "",
    `Body: ${params.signal.body}`,
    "",
    "## RECIPIENT",
    `Name: ${params.signal.contact_name}`,
    params.signal.contact_role ? `Role: ${params.signal.contact_role}` : "",
    params.signal.contact_company ? `Company: ${params.signal.contact_company}` : "",
    "",
    "Now write the email. Return strictly { subject, body }.",
  ]
    .filter(Boolean)
    .join("\n")
    .slice(0, TOTAL_PROMPT_CAP);

  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), GEMINI_TIMEOUT_MS);
  try {
    const result = await model.generateContent(userPrompt);
    const text = result.response.text();
    const parsed = JSON.parse(text) as { subject?: unknown; body?: unknown };
    const subject = typeof parsed.subject === "string" ? parsed.subject.trim() : "";
    const body = typeof parsed.body === "string" ? parsed.body.trim() : "";
    if (!subject || !body) {
      return { ...templateDraft(params.signal, evidence), fallback_reason: "empty fields in LLM response" };
    }
    return { subject, body, evidence, source: "gemini" };
  } catch (err) {
    const reason = err instanceof Error ? err.message : "unknown error";
    return { ...templateDraft(params.signal, evidence), fallback_reason: `gemini error: ${reason}` };
  } finally {
    clearTimeout(timer);
  }
}
