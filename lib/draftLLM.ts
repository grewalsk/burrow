// Real draft generator: Gemini Flash Lite via the same API key as cr_agent.
// Pulls grounding evidence from ZE, builds a structured prompt, returns a
// strict JSON { subject, body }. Falls back to a deterministic template if
// the API key is missing or the call fails — the flow never blocks.

import { GoogleGenerativeAI, SchemaType } from "@google/generative-ai";
import {
  getDocumentContent,
  listDocuments,
  topDocuments,
} from "./zeroentropyClient";

const GEMINI_TIMEOUT_MS = 20_000;
const MAX_GROUNDING_DOCS = 4;
const MAX_GROUNDING_CHARS_PER_DOC = 1_200;
const TOTAL_PROMPT_CAP = 12_000;

const DRAFT_SCHEMA = {
  type: SchemaType.OBJECT,
  properties: {
    subject: { type: SchemaType.STRING },
    body: { type: SchemaType.STRING },
  },
  required: ["subject", "body"],
};

export type SignalInfo = {
  signal_id: string;
  source: string;
  handle: string;
  context?: string;
  body: string;
  contact_name: string;
  contact_role?: string;
  contact_company?: string;
};

export type GeneratedDraft = {
  subject: string;
  body: string;
  evidence: string[];
  source: "gemini" | "fallback";
  fallback_reason?: string;
};

function firstName(full: string): string {
  return (full.split(" ")[0] ?? full).trim() || "there";
}

function shortQuote(body: string, max = 200): string {
  const trimmed = body.replace(/\s+/g, " ").trim();
  if (trimmed.length <= max) return trimmed;
  const cut = trimmed.slice(0, max);
  const space = cut.lastIndexOf(" ");
  return (space > 0 ? cut.slice(0, space) : cut) + "…";
}

function templateDraft(signal: SignalInfo, evidence: string[]): GeneratedDraft {
  const first = firstName(signal.contact_name);
  const quote = shortQuote(signal.body, 140);
  const sourceLabel =
    signal.source === "HACKER NEWS" ? "your HN post" : `your ${signal.source.toLowerCase()} post`;
  const subject =
    /pinecone|vector db|managed vector/i.test(signal.body)
      ? "On the Pinecone math"
      : /soc2|compliance|fintech/i.test(signal.body)
        ? "Compliance question — quick note"
        : /migrate|switching|moving off/i.test(signal.body)
          ? "Migration playbook — saw your post"
          : "Saw your post — one quick thought";
  const body = [
    `${first},`,
    "",
    `Saw ${sourceLabel}: "${quote}"`,
    "",
    "We've seen this pattern a few times — usually the trigger isn't the bill, it's the on-call cost. The math flips fast.",
    "",
    "If a 15-minute look at the numbers would be useful, I can pull a side-by-side from one of our recent migrations. No deck, no demo — just the numbers.",
    "",
    "— our team",
  ].join("\n");
  return { subject, body, evidence, source: "fallback", fallback_reason: "GOOGLE_GENAI_API_KEY not set" };
}

